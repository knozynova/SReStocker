#!/bin/bash
# ==============================================================================
# SReStocker - Floating Features Script
# Contains:
#   1. APPLY_STOCK_ROM_FLOATING_FEATURE — copies device-specific values from
#      the stock floating_feature.xml into the target ROM
#   2. APPLY_CUSTOM_FLOATING_FEATURES — your custom tweaks on top
#
# How to use APPLY_CUSTOM_FLOATING_FEATURES:
#   - To ADD a feature:    add a new UPDATE_FLOATING_FEATURE line
#   - To REMOVE a feature: comment the line out with #
#   - To EDIT a feature:   change the value
#   - To DELETE a key:     use DELETE_FLOATING_FEATURE "KEY_NAME"
# ==============================================================================

: "${YELLOW:=\e[33m}"
: "${NC:=\e[0m}"

DELETE_FLOATING_FEATURE() {
    local key="$1"
    echo "- Deleting floating feature key: $key"
    sed -i "/<${key}>/d" "$TARGET_ROM_FLOATING_FEATURE"
}

APPLY_STOCK_ROM_FLOATING_FEATURE() {
